#include<stdio.h>

int main()
{
    int h, b;
    float area;
    printf("\n\nEnter the height of the Triangle: ");
    scanf("%d", &h);
    printf("\n\nEnter the base of the Triangle: ");
    scanf("%d", &b);
    area = (h*b)/(float)2;
    printf("\n\n\nThe area of the triangle is: %f", area);
    return 0;
}